-module(deploy_track_sup).
-author("hazen").

-behaviour(supervisor).

%% API
-export([start_link/0]).

%% Supervisor callbacks
-export([init/1]).

-define(SERVER, ?MODULE).

%%%===================================================================
%%% API functions
%%%===================================================================

%%--------------------------------------------------------------------
%% @doc
%% Starts the supervisor
%%
%% @end
%%--------------------------------------------------------------------
-spec(start_link() ->
    {ok, Pid :: pid()} | ignore | {error, Reason :: term()}).
start_link() ->
    supervisor:start_link({global, ?SERVER}, ?MODULE, []).

%%%===================================================================
%%% Supervisor callbacks
%%%===================================================================

%%--------------------------------------------------------------------
%% @private
%% @doc
%% Whenever a supervisor is started using supervisor:start_link/[2,3],
%% this function is called by the new process to find out about
%% restart strategy, maximum restart frequency and child
%% specifications.
%%
%% Here we are starting up the PackageCloud.io, Amazon S3 and
%% Google Analytics worker processes.
%%
%% @end
%%--------------------------------------------------------------------
-spec(init(Args :: term()) ->
    {ok, {SupFlags :: {RestartStrategy :: supervisor:strategy(),
        MaxR :: non_neg_integer(), MaxT :: non_neg_integer()},
        [ChildSpec :: supervisor:child_spec()]
    }} |
    ignore |
    {error, Reason :: term()}).
init([]) ->
    RestartStrategy = one_for_one,
    MaxRestarts = 1000,
    MaxSecondsBetweenRestarts = 3600,

    SupFlags = {RestartStrategy, MaxRestarts, MaxSecondsBetweenRestarts},

    Restart = permanent,
    Shutdown = 2000,
    Type = worker,

    CloudWorker = {cloud_id, {deploy_track_pkgcloud, start_link, []},
        Restart, Shutdown, Type, [deploy_track_pkgcloud]},
    S3Worker = {s3_id, {deploy_track_s3, start_link, []},
        Restart, Shutdown, Type, [deploy_track_s3]},
    AnalyticsWorker = {analtyics_id, {deploy_track_analytics, start_link, []},
        Restart, Shutdown, Type, [deploy_track_analytics]},

    {ok, {SupFlags, [CloudWorker, S3Worker, AnalyticsWorker]}}.

